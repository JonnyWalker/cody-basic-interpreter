def parse(s):
    result = []
    for line in s.splitlines():
        if line.startswith(".BYTE "):
            char_data = []
            for b in line[len(".BYTE ") :].split(","):
                b = b.strip()
                assert b.startswith("$")
                n = int(b[1:], base=16)
                assert 0 <= n < 256
                char_data.append(n)
            assert len(char_data) == 8
            result.append(bytes(char_data))
    return tuple(result)


